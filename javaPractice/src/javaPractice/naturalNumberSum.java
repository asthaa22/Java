package javaPractice;

public class naturalNumberSum {
	public static void main (String[]args)
	  {
	        int n=5,sum = 0;
	        for (int i = 1; i <= n; i++)
	        {
	    	  for (int j = 1; j <= i; j++)
	    	  {
	    	    sum++;
	          }
	              
	        }
	      System.out.println(sum);
	      
	  }
	
}
